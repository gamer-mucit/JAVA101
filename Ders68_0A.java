public class Ders68_0A {
    static void print(int[] arr){
        System.out.print("[ ");
        for(int i = 0; i < arr.length ; i++){
            System.out.print(arr[i] + " ");
        }
        System.out.print("]");
    }

    void print(double[] arr){
        System.out.print("[ ");
        for(int i = 0; i < arr.length ; i++){
            System.out.print(arr[i] + " ");
        }
        System.out.print("]");
    }
}
