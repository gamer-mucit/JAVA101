public class Ders21 {
    public static void main(String[] args) {
        int a =10 ,d =40,b =20,c =10;
        System.out.print("== Operatörü \n");
        System.out.println(a == d);
        System.out.println(a == c);
        System.out.println(c == b);
        System.out.print("!= Eşit Değil Operatörü \n");
        System.out.println(a != d);
        System.out.println(a != c);
        System.out.println(c != b);
        System.out.print("> Büyüktür Operatörü \n");
        System.out.println(a > d);
        System.out.println(d > c);
        System.out.println(c > b);
        System.out.print(">= Büyük-Eşittir Operatörü \n");
        System.out.println(a >= d);
        System.out.println(a >= c);
        System.out.println(c >= b);
        System.out.print("< Küçüktür Operatörü \n");
        System.out.println(a < d);
        System.out.println(d < c);
        System.out.println(c < b);
        System.out.print("<= Küçük-Eşittir Operatörü \n");
        System.out.println(a <= d);
        System.out.println(a <= c);
        System.out.println(c <= b);

    }
}
